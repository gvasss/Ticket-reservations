package Cinema;

public class Cinemas {

	private int ID;
	private String NAME;
	private String SEATS;
	private String D3;
	
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	
	public String getNAME() {
		return NAME;
	}
	public void setNAME(String nAME) {
		NAME = nAME;
	}
	
	public String getSEATS() {
		return SEATS;
	}
	public void setSEATS(String sEATS) {
		SEATS = sEATS;
	}
	
	public String getD3() {
		return D3;
	}
	public void setD3(String d3) {
		D3 = d3;
	}

}
